    private void IngresarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarBActionPerformed
        this.user = this.EmailText.getText();
        this.pass = this.TextPass.getText();
        if (user.isEmpty() || pass.isEmpty()) {
            this.HeaderWelcome.setForeground(Color.red);
            this.HeaderWelcome.setText("Los espacios no pueden estar en blanco >:|");
        } else {
            String sentencia = "%session" + "-SELECT tbl_Registro.Contra_Usuario_Registro,tbl_Registro.Nombre FROM tbl_Registro WHERE tbl_Registro.Correo_Usu = " + "'" + user + "'" + "-" + pass;

            S = new Send2Server(DB.getSocket(), sentencia, this);
            S.start();
            s1 = new Read4ServerThr(DB.getServerin(), this);
            s1.start();

        }
    }//GEN-LAST:event_IngresarBActionPerformed

