    private void IngresarBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_IngresarBMouseClicked
        if (evt.getClickCount() == 2 && evt.getButton() == MouseEvent.BUTTON1) {
            //siguiente frame;

            if (this.response.contains("1")) {
                String[] parts = this.response.split("-");
                this.login = parts[1];
                this.setVisible(false);
                this.guichats = new GUIChats(this);
                guichats.setVisible(true);
            } else {
                this.HeaderWelcome.setForeground(Color.red);
                this.HeaderWelcome.setText("La contrase√±a o el correo estan incorrectos :(");
            }

        }
    }//GEN-LAST:event_IngresarBMouseClicked

